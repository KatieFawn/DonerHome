package com.jiromo5.donerhome.data.state;

/**
 * The CartsData class stores the quantities of items in the cart.
 * This class contains static variables to track the counts of Cola and CheeseBurger in the cart.
 */
public class CartsData {

    /**
     * Static variable to store the count of Cola in the cart.
     * It is initialized to 0 by default and can be updated when items are added to or removed from the cart.
     */
    public static int countOfCola;

    /**
     * Static variable to store the count of CheeseBurger in the cart.
     * It is initialized to 0 by default and can be updated when items are added to or removed from the cart.
     */
    public static int countOfCheeseBurger;
}
